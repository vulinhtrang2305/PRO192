public class bottle {
    private String tittle;
    private int price;

    public bottle() {
        super();
    }

    public bottle(String tittle, int price) {
        this.tittle = tittle;
        this.price = price;
    }

    public String getTittle() {
        return tittle;
    }

    public int getPrice() {
        return price;
    }

    public void setTittle(String tittle) {
        this.tittle = tittle;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    
    
    
}
